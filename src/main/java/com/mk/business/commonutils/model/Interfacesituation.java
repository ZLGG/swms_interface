package com.mk.business.commonutils.model;

import java.util.Date;

public class Interfacesituation {
    private  String  interfaceSituationGuid;
    private  String  interfaceName;
    private  String  interfaceUrl;
    private  Date   interfaceDate;
    private  String interfaceState;
    private  String interfaceSituation;
    private  String interfaceDesc;

    public String getInterfaceSituationGuid() {
        return interfaceSituationGuid;
    }

    public void setInterfaceSituationGuid(String interfaceSituationGuid) {
        this.interfaceSituationGuid = interfaceSituationGuid;
    }

    public String getInterfaceName() {
        return interfaceName;
    }

    public void setInterfaceName(String interfaceName) {
        this.interfaceName = interfaceName;
    }

    public String getInterfaceUrl() {
        return interfaceUrl;
    }

    public void setInterfaceUrl(String interfaceUrl) {
        this.interfaceUrl = interfaceUrl;
    }

    public Date getInterfaceDate() {
        return interfaceDate;
    }

    public void setInterfaceDate(Date interfaceDate) {
        this.interfaceDate = interfaceDate;
    }

    public String getInterfaceState() {
        return interfaceState;
    }

    public void setInterfaceState(String interfaceState) {
        this.interfaceState = interfaceState;
    }

    public String getInterfaceSituation() {
        return interfaceSituation;
    }

    public void setInterfaceSituation(String interfaceSituation) {
        this.interfaceSituation = interfaceSituation;
    }

    public String getInterfaceDesc() {
        return interfaceDesc;
    }

    public void setInterfaceDesc(String interfaceDesc) {
        this.interfaceDesc = interfaceDesc;
    }
}
